package com.koreait.app.community_Review_Board.vo;

public class Community_Review_BoardVO {
	//감동후기 게시판
	private int board_num;
	private String board_title;
	private String board_content;
	private String board_id;
	private String board_date;
	private int readCount;
	private String board_img_name;
	
	public Community_Review_BoardVO() {;}

	public int getBoard_num() {
		return board_num;
	}

	public void setBoard_num(int board_num) {
		this.board_num = board_num;
	}

	public String getBoard_title() {
		return board_title;
	}

	public void setBoard_title(String board_title) {
		this.board_title = board_title;
	}

	public String getBoard_content() {
		return board_content;
	}

	public void setBoard_content(String board_content) {
		this.board_content = board_content;
	}

	public String getBoard_id() {
		return board_id;
	}

	public void setBoard_id(String board_id) {
		this.board_id = board_id;
	}

	public String getBoard_date() {
		return board_date;
	}

	public void setBoard_date(String board_date) {
		this.board_date = board_date;
	}

	public int getReadCount() {
		return readCount;
	}

	public void setReadCount(int readCount) {
		this.readCount = readCount;
	}

	public String getBoard_img_name() {
		return board_img_name;
	}

	public void setBoard_img_name(String board_img_name) {
		this.board_img_name = board_img_name;
	}
	
}
